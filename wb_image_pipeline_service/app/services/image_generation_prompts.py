"""Final image-generation prompt wrappers for WIP product photos."""

from __future__ import annotations


def build_main_image_prompt(main_prompt: str) -> str:
    return f"""Сгенерируй реалистичное главное фото товара для карточки Wildberries на основе переданных исходных референсов.

Следуй этому промпту кадра:
{main_prompt}

Строго сохранить товар:
- та же категория товара;
- тот же цвет;
- тот же материал и фактура;
- та же форма, крой, конструкция и пропорции;
- те же ключевые видимые детали с референса;
- не превращай товар в другой предмет;
- не добавляй логотипы, надписи и детали самого товара, которых нет на референсе.

Это этап выбора визуального направления будущей фотосессии. Можно менять модель, фон, локацию, позу, ракурс, крупность, свет, композицию, нижнюю одежду, обувь, аксессуары и общий styling, если сам товар остаётся точным и хорошо читаемым.

Не делай белый/нейтральный фон по умолчанию, если промпт кадра просит lifestyle/editorial сцену.

Фотореализм, чистая композиция, современное e-commerce качество, готово для карточки маркетплейса."""


def build_content_image_prompt(series_prompt: str) -> str:
    return f"""Сгенерируй одно изображение как следующий кадр из той же самой фотосессии Wildberries, что и выбранное главное фото.

Следуй этому промпту кадра:
{series_prompt}

Жёстко сохранить:
- ту же модель;
- ту же внешность, лицо, волосы, телосложение и общий образ модели;
- ту же локацию;
- тот же фон;
- тот же лук/стайлинг;
- ту же одежду/товарную подачу;
- тот же световой стиль и цветовое настроение;
- тот же товар;
- тот же цвет, материал, фактуру, форму, фасон, крой, пропорции и ключевые детали товара.

Запрещено:
- менять модель;
- менять локацию или фон;
- менять лук или styling;
- менять товар;
- менять цвет, материал, фасон, форму или конструкцию товара;
- создавать новую фотосессию;
- добавлять новые предметы, логотипы, случайные надписи или несуществующие детали.

Можно менять только то, что просит промпт кадра:
- ракурс камеры;
- крупность;
- позу модели или положение товара;
- кадрирование;
- сторону показа товара;
- фокус на конкретной детали товара;
- аккуратную инфографику, только если она явно указана в промпте кадра.

Фотореалистичное изображение, единое с выбранным главным фото, готовое для карточки Wildberries."""
